#!/usr/bin/env python3
"""
This module defines a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenate two arrays.

    Args:
        arr1 (list of ints/floats): The first array.
        arr2 (list of ints/floats): The second array.

    Returns:
        list of ints/floats: list that is the concatenation of arr1 and arr2.
    """
    return arr1 + arr2
